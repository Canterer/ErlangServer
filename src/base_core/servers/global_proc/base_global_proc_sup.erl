-module(base_global_proc_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1,
	 start_checker/0
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_checker()->
	supervisor:start_link({local,?SERVER}, ?MODULE, [checker]).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([DbType]) ->
	case DbType of
		checker->
			AChildList = [{base_global_proc_checker_server,{base_global_proc_checker_server,start_link,[]}, transient,2000,worker,[base_global_proc_checker_server]}];
		_->
			AChildList = []
	end,
    {ok,{{one_for_all,10,10}, AChildList}}.

